import mongoose from "mongoose";

const todoSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  dueDate: {
    type: Date,
    required: true,
  },
  completed: {
    type: Boolean,
    default: false,
  },
  user:{
    ref: "user",
    type:  new mongoose.Types.ObjectId()
  }
});

export const Todo = mongoose.model("todo", todoSchema);
